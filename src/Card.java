public class Card {
    // Attributes
    private int nr;
    private String color;

    // Constructor
    public Card(int nr, String color) {
        this.nr = nr;
        this.color = color;
    }

    // Getter and Setters
    public int getNr() {
        return nr;
    }

    public String getColor() {
        return color;
    }
}
